import pandas as pd
import re

# =====================================================
# 1. 원본 문화재 상세 데이터 불러오기
# =====================================================

heritage_df = pd.read_csv(
    "data/processed/yc_heritage_detail_enriched.csv"
)

# 컬럼 공백 제거
heritage_df.columns = (
    heritage_df.columns.str.strip()
)

# =====================================================
# 2. 문화재 특성 데이터셋 생성
# =====================================================

feature_df = heritage_df[[

    "문화재명(국문)",
    "국가유산종목",
    "시대",
    "소재지상세",
    "위도",
    "경도"

]].copy()

# =====================================================
# 3. 시대 그룹화 함수
# =====================================================

def simplify_era(text):

    if pd.isna(text):
        return "기타"

    text = str(text).strip()

    # -------------------------------------------------
    # 청동기
    # -------------------------------------------------

    if "청동기" in text:
        return "청동기"

    # -------------------------------------------------
    # 신라
    # -------------------------------------------------

    elif (
        "통일신라" in text or
        "신라시대 후기" in text
    ):
        return "통일신라"

    elif "신라" in text:
        return "신라"

    # -------------------------------------------------
    # 고려
    # -------------------------------------------------

    elif (
        "고려시대 초기" in text or
        "고려 초기" in text
    ):
        return "고려초기"

    elif (
        "고려시대 말기" in text or
        "고려 말기" in text
    ):
        return "고려후기"

    elif "고려" in text:
        return "고려"

    # -------------------------------------------------
    # 조선 초기
    # -------------------------------------------------

    elif (
        "세종" in text or
        "태조" in text or
        "태종" in text or
        "세조" in text or
        "성종" in text or
        "중종" in text or
        "인종" in text
    ):
        return "조선초기"

    # -------------------------------------------------
    # 조선 후기
    # -------------------------------------------------

    elif (
        "숙종" in text or
        "영조" in text or
        "정조" in text or
        "순조" in text or
        "철종" in text or
        "고종" in text or
        "광해군" in text
    ):
        return "조선후기"

    # -------------------------------------------------
    # 조선 직접 표기
    # -------------------------------------------------

    elif (
        "조선시대 후기" in text or
        "조선 후기" in text
    ):
        return "조선후기"

    elif "조선" in text:
        return "조선"

    # -------------------------------------------------
    # 대한제국
    # -------------------------------------------------

    elif "대한제국" in text:
        return "대한제국"

    # -------------------------------------------------
    # 연도 기반 추정
    # -------------------------------------------------

    year_match = re.search(
        r"\d{4}",
        text
    )

    if year_match:

        year = int(
            year_match.group()
        )

        if year < 700:
            return "신라"

        elif year < 1400:
            return "고려"

        elif year < 1600:
            return "조선초기"

        elif year < 1910:
            return "조선후기"

    return "기타"

# =====================================================
# 4. 시대그룹 컬럼 생성
# =====================================================

feature_df["시대그룹"] = (
    feature_df["시대"]
    .apply(simplify_era)
)

# =====================================================
# 5. 기본 컬럼 생성
# =====================================================

feature_df["재질"] = "미분류"
feature_df["노출형태"] = "미분류"

# =====================================================
# 6. 재질 분류 키워드
# =====================================================

stone_keywords = [

    "석탑",
    "석조",
    "석불",
    "비",
    "암각화",
    "태실",
    "옥비"

]

wood_keywords = [

    "고택",
    "서원",
    "향교",
    "정",
    "당",
    "각",
    "재",
    "대웅전",
    "종택",
    "사당"

]

painting_keywords = [

    "탱화",
    "괘불",
    "초상",
    "영정",
    "도"

]

paper_keywords = [

    "고문서",
    "판목",
    "조보",
    "론",
    "집",
    "경"

]

metal_keywords = [

    "철조",
    "금동",
    "청동",
    "범종"

]

nature_keywords = [

    "은행나무",
    "회화나무",
    "장림"

]

building_keywords = [

    "가옥",
    "교회",
    "급수탑",
    "교"

]

# =====================================================
# 7. 재질 자동 분류
# =====================================================

for idx, row in feature_df.iterrows():

    name = str(
        row["문화재명(국문)"]
    )

    # -------------------------------------------------
    # 석조
    # -------------------------------------------------

    if any(
        keyword in name
        for keyword in stone_keywords
    ):

        feature_df.loc[idx, "재질"] = "석조"

    # -------------------------------------------------
    # 목조
    # -------------------------------------------------

    elif any(
        keyword in name
        for keyword in wood_keywords
    ):

        feature_df.loc[idx, "재질"] = "목조"

    # -------------------------------------------------
    # 회화
    # -------------------------------------------------

    elif any(
        keyword in name
        for keyword in painting_keywords
    ):

        feature_df.loc[idx, "재질"] = "회화"

    # -------------------------------------------------
    # 지류
    # -------------------------------------------------

    elif any(
        keyword in name
        for keyword in paper_keywords
    ):

        feature_df.loc[idx, "재질"] = "지류"

    # -------------------------------------------------
    # 금속
    # -------------------------------------------------

    elif any(
        keyword in name
        for keyword in metal_keywords
    ):

        feature_df.loc[idx, "재질"] = "금속"

    # -------------------------------------------------
    # 자연유산
    # -------------------------------------------------

    elif any(
        keyword in name
        for keyword in nature_keywords
    ):

        feature_df.loc[idx, "재질"] = "자연유산"

    # -------------------------------------------------
    # 건축
    # -------------------------------------------------

    elif any(
        keyword in name
        for keyword in building_keywords
    ):

        feature_df.loc[idx, "재질"] = "건축"

# =====================================================
# 8. 노출형태 분류 키워드
# =====================================================

indoor_keywords = [

    "탱화",
    "괘불",
    "초상",
    "고문서",
    "판목",
    "조보",
    "경",
    "론"

]

outdoor_keywords = [

    "석탑",
    "비",
    "암각화",
    "봉수",
    "태실",
    "장림",
    "회화나무",
    "은행나무"

]

semi_keywords = [

    "고택",
    "서원",
    "향교",
    "대웅전",
    "정",
    "당",
    "재",
    "종택",
    "가옥",
    "교회"

]

# =====================================================
# 9. 노출형태 자동 분류
# =====================================================

for idx, row in feature_df.iterrows():

    name = str(
        row["문화재명(국문)"]
    )

    # -------------------------------------------------
    # 실내
    # -------------------------------------------------

    if any(
        keyword in name
        for keyword in indoor_keywords
    ):

        feature_df.loc[idx, "노출형태"] = "실내"

    # -------------------------------------------------
    # 실외
    # -------------------------------------------------

    elif any(
        keyword in name
        for keyword in outdoor_keywords
    ):

        feature_df.loc[idx, "노출형태"] = "실외"

    # -------------------------------------------------
    # 반실외
    # -------------------------------------------------

    elif any(
        keyword in name
        for keyword in semi_keywords
    ):

        feature_df.loc[idx, "노출형태"] = "반실외"

# =====================================================
# 10. 결과 확인
# =====================================================

print(feature_df.head())

print("\n재질 분포")
print(
    feature_df["재질"]
    .value_counts()
)

print("\n노출형태 분포")
print(
    feature_df["노출형태"]
    .value_counts()
)

# =====================================================
# 11. 저장
# =====================================================

feature_df.to_csv(

    "data/processed/yc_heritage_feature.csv",

    index=False,
    encoding="utf-8-sig"

)

print("\n문화재 특성 데이터셋 저장 완료")
