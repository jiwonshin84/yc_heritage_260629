import streamlit as st
import pandas as pd

# ==========================================================
# 페이지 설정
# ==========================================================
st.set_page_config(
    page_title="문화재 위험도 AI 분석",
    page_icon="🏛",
    layout="wide"
)

# ==========================================================
# 데이터 불러오기
# ==========================================================
df = pd.read_csv(
    "data/processed/yc_heritage_detail_enriched.csv"
)

# ==========================================================
# 제목
# ==========================================================
st.title("🏛 공공데이터 기반 문화재 훼손 위험 예측 시스템")

st.markdown("""
### 문화재 + 환경데이터 + AI 분석 기반 연구 프로젝트

공공데이터를 활용하여  
문화재의 환경 노출 위험을 분석하고  
AI 기반 훼손 위험 예측 모델을 개발합니다.
""")

st.divider()

# ==========================================================
# 주요 통계 카드
# ==========================================================
st.subheader("📊 주요 통계")

c1, c2, c3, c4 = st.columns(4)

# 전체 문화재 수
c1.metric(
    "영천 문화재 수",
    len(df)
)

# 국보·보물 수
c2.metric(
    "국보·보물 수",
    len(
        df[
            df["국가유산종목"]
            .isin(["국보", "보물"])
        ]
    )
)

# 시대 종류 수
c3.metric(
    "시대 종류",
    df["시대"].nunique()
)

# 문화재 유형 수
c4.metric(
    "문화재 유형",
    df["국가유산종목"].nunique()
)

st.divider()

# ==========================================================
# 페이지 안내
# ==========================================================
st.subheader("📚 페이지 안내")

st.info("""
왼쪽 사이드바 메뉴를 통해
각 분석 페이지로 이동할 수 있습니다.
""")

